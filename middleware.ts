import { createServerClient } from "@supabase/ssr"
import { NextResponse, type NextRequest } from "next/server"

export async function middleware(request: NextRequest) {
  let response = NextResponse.next({
    request: {
      headers: request.headers,
    },
  })

  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return request.cookies.getAll()
        },
        setAll(cookiesToSet) {
          cookiesToSet.forEach(({ name, value, options }) => request.cookies.set(name, value))
          response = NextResponse.next({
            request,
          })
          cookiesToSet.forEach(({ name, value, options }) => response.cookies.set(name, value, options))
        },
      },
    },
  )

  const {
    data: { session },
  } = await supabase.auth.getSession()

  const isLoginPage = request.nextUrl.pathname === "/login"

  console.log("ğŸ” MIDDLEWARE:", {
    path: request.nextUrl.pathname,
    hasSession: !!session,
    isLoginPage,
  })

  // ë¡œê·¸ì¸í•˜ì§€ ì•Šì€ ì‚¬ìš©ìê°€ ë³´í˜¸ëœ í˜ì´ì§€ì— ì ‘ê·¼í•˜ë ¤ëŠ” ê²½ìš°
  if (!session && !isLoginPage) {
    console.log("ğŸš« No session, redirecting to /login")
    return NextResponse.redirect(new URL("/login", request.url))
  }

  // ì´ë¯¸ ë¡œê·¸ì¸í•œ ì‚¬ìš©ìê°€ ë¡œê·¸ì¸ í˜ì´ì§€ì— ì ‘ê·¼í•˜ë ¤ëŠ” ê²½ìš°
  if (session && isLoginPage) {
    console.log("âœ… Has session, redirecting to /")
    return NextResponse.redirect(new URL("/", request.url))
  }

  console.log("âœ… MIDDLEWARE: Allowing request")

  return response
}

export const config = {
  matcher: [
    /*
     * Match all request paths except for the ones starting with:
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     * - public folder
     * - /auth/ (auth callbacks)
     */
    "/((?!_next/static|_next/image|favicon.ico|auth/|.*\\.(?:svg|png|jpg|jpeg|gif|webp)$).*)",
  ],
}
